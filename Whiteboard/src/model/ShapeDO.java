package model;

public class ShapeDO {
	private int shapeKey;
	private String shapeString;

	public ShapeDO() {

	}
	
	public int getShapeKey() {
		return shapeKey;
	}

	public void setShapeKey(int shapeKey) {
		this.shapeKey = shapeKey;
	}

	public String getShapeString() {
		return shapeString;
	}

	public void setShapeString(String shapeString) {
		this.shapeString = shapeString;
	}
}
